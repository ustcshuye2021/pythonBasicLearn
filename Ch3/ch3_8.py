# 字符串的拼接
num1 = 222
num2 = 333
num3 = num1 + num2
print("以下是数值相加")
print(num3)

numstr1 = "222"
numstr2 = "333"
numstr3 = numstr1 + numstr2
print("以下是由数值组成的字符串相加")
print(numstr3)

numstr4 = numstr1 + " " + numstr2
print("以下是由数值组成的字符串相加，同时中间加上一空格")
print(numstr4)

str1 = "DeepStone"
str2 = "Deep Learning"
str3 = str1 + str2
print("以下是一般字符串相加")
print(str3)
