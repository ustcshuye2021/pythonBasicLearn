hourly_salary = 125                              # 设置时薪
annual_salary = hourly_salary * 8 * 300          # 计算年薪
monthly_fee = 9000                               # 设置每月花费
annual_fee = monthly_fee * 12                    # 设置每年花费
annual_savings = annual_salary - annual_fee      # 计算每年存储金额
print(annual_savings)                            # 列出每年存储金额
